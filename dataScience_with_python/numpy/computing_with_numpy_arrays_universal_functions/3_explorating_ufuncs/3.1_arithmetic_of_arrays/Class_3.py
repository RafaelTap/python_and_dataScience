import numpy as np

# all arithmetic operations can be used.

x = np.arange(4)

print(x)
print("x + 5 = ", x + 5)
print("x - 4 = ", x - 4)
print("x * 3 = ", x * 3)
print("x / 2 = ", x / 2)
print("x // 2 = ", x // 2) # whole division

print("-------------------")

print("x ** 2 = ", x ** 2)
print("x % 2 = ", x % 2)
