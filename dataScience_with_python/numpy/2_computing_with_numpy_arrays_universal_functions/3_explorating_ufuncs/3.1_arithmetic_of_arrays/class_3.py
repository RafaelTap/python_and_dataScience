import numpy as np

# all arithmetics operations can be used.

x = np.arange(4)

print(x)
print("x + 5 = ", x + 5) # np.add(array, object)
print("x - 4 = ", x - 4) # np.subtract(array, object)
print("x * 3 = ", x * 3) # np.multiplay()
print("x / 2 = ", x / 2) # np.divide()
print("x // 2 = ", x // 2) # whole division np.floor_divide

print("-------------------")

print("-x = ", -x) # np.negative()
print("x ** 2 = ", x ** 2) # np.power()
print("x % 2 = ", x % 2) # np.mod()
